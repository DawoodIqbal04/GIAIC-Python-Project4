
import random

min_num: int = 1
max_num: int = 10

def main():
    random_num: int = random.randint(min_num, max_num)
    computer_num: int = random.randint(min_num, max_num)

    while computer_num != random_num:
        print(f"Computers Guess: {computer_num}")
        print("This Number Is A Wrong Guess!")
        print('')
        computer_num: int = random.randint(min_num, max_num)
    
    print(f"Computer Guess: {computer_num} is equal to random number: {random_num}")


if __name__ == "__main__":
    main()
