
def main():
    side1 : float = float(input("Enter the length of the first side of triangle: "))
    side2 : float = float(input("Enter the length of the second side of triangle: "))
    side3 : float = float(input("Enter the length of the third side of triangle: "))

    print(f"The Perimeter of the triangle is {str(side1 + side2 + side3)}")

if __name__ == "__main__":
    main()
